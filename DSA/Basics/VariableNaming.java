public class VariableNaming {
    public static void main(String[] args) {
        

        // those are not allowed for varname 
        // int 2value = 100;     // ❌ starts with digit
        // int my-value = 50;    // ❌ contains hyphen
        // int class = 10;       // ❌ reserved keyword
        // int user name = 5;    // ❌ contains space


        // allows else only 
            // _ or alphabet or $ 

            int $and = 4 ; 

            System.out.println($and);

    }

    
}
