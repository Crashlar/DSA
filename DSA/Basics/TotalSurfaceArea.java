
public class TotalSurfaceArea {
    
    public static void main(String[] args) {
        
        // intialize a float  variable
        float l = 12 , b = 23  ;
        double h = 34.5 ; 


        // calculating TSA
        double TSA =2 * (l* b + b * h + h * l ); 
        System.out.print("Total Surface Area of Sphere : ");
        System.out.println(TSA);
    }
    
}
