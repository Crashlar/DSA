public class IncrementDecrement {

    public static void main(String[] args) {
        
        int a = 10 ; 
        System.out.println(a);
        a += 1 ; 
        System.out.println(a);
        // pre increment
        ++a;
        System.out.println(a);

        System.out.println(a);

        a *= 4 ; 
        System.out.println(a);
        // post increment
        a++;
        System.out.println(a);
        System.out.println(a);
    
        // post decrement
        a--;
        System.out.println(a);
        System.out.println(a);

        // pre decrement
        --a;
        System.out.println(a);

        System.out.println(a);
    }
    
}
